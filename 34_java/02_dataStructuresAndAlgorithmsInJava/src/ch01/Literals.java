package ch01;

public class Literals {
	/**
	 * Notes:
	 * - A literal is any “constant” value that can be used in an assignment or other expression
	 * - The null object reference is the only object literal, and it is allowed to be any reference type
	 * - Java defines the following special character constants:
	 * '\n' (newline)
	 * '\t' (tab)
	 * '\b' (backspace)
	 * '\r' (return)
	 * '\f' (form feed)
	 * '\\' (backslash)
	 * '\'' (single quote)
	 * '\"' (double quote)
	 */

	// Java allows the following kinds of literals:
	Counter counter = null; // The null object reference
	boolean aBoolean = true; // true or false
	int anInt = 127; // int
	long aLong = 127L; // long
	float aFloat = 1.27F; // float
	double aDouble = 1.27D; // double
	char aChar = 'J'; // char
	String aString = "dogs cannot climb trees"; // string
}
